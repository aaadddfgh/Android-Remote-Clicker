package mm.pp.clicker.tools;

public class ActionPoint {
    public int x;
    public int y;

    public ActionPoint(Integer x, Integer y) {
        this.x = x;
        this.y = y;
    }


    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }
}
